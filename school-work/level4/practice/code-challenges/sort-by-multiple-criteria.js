function sortByMultipleCriteria(people) {
  const nameSort = people.sort((a, b) => {
    if (a.name < b.name) {
      return -1
    }
    if (a.name > b.name) {
      return 1
    }
      return 0
    })
  return nameSort.sort((a, b) => {
    if(a.age < b.age){
      return -1
    }
    if(a.age > b.age){
      return 1
    }
    return 0
  })
}

const people = [
{ name: 'Alice', age: 30 },
{ name: 'Bob', age: 25 },
{ name: 'Charlie', age: 35 },
{ name: 'David', age: 25 },
];

const sortedPeople = sortByMultipleCriteria(people);
console.log(sortedPeople);

